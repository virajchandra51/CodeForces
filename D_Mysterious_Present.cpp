ll lcm(ll a, ll b){ return (a/__gcd(a,b))*b;}
ll dp[N],pr[N];
void solve(){
	ll n,W,H;
	cin>>n>>W>>H;
	vector<pair<ll,pair<ll,ll>>>a;
	for(int i=1; i<=n; i++){
		ll w,h;
		cin>>w>>h;
		if(W<w && H<h){
			a.pb({w,{h,i}});
		}
	}
	sort(a.begin(),a.end());
	ll ans=0,pos=0;
	for(int i=0; i<a.size(); i++){
		dp[a[i].S.S]=1;
		pr[a[i].S.S]=-1;
		for(int j=0; j<i; j++){
			if(a[j].F<a[i].F && a[j].S.F<a[i].S.F && dp[a[j].S.S]+1>dp[a[i].S.S]){
				dp[a[i].S.S]=dp[a[j].S.S]+1;
				pr[a[i].S.S]=a[j].S.S;
			}
		}
	}
	for(int i=1; i<=n; i++){
		if(dp[i]>ans){
			ans=dp[i];
			pos=i;
		}
	}
	vector<ll>p;
	while(pos!=0&&pos!=-1){
		p.pb(pos);
		pos=pr[pos];
		// cout<<pos<<endl;
	}cout<<ans<<endl;
	for(int i=p.size()-1; i>=0; i--){
		cout<<p[i]<<' ';
	}cout<<endl;
}
